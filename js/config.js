// カードLv -> スキルLv (1~5) 変換
function getSkillLevelFromCardLevel(rarity, level) {
    const lvl = parseInt(level);
    if (rarity === 'SSR') {
        if (lvl >= 50) return 5;
        if (lvl >= 45) return 4;
        if (lvl >= 40) return 3;
        if (lvl >= 35) return 2;
        return 1; // Lv30以下
    } else {
        // SR
        if (lvl >= 45) return 5;
        if (lvl >= 40) return 4;
        if (lvl >= 35) return 3;
        if (lvl >= 30) return 2;
        return 1; // Lv25以下
    }
}

